export function getZoomLevel(zoomState) {
  if (!zoomState || !zoomState.zoom || zoomState.zoom < 2) {
    return 'country';
  }
  if (zoomState.zoom < 8) {
    return 'state';
  }
  // Note that zoom cannot be more than 8, we set max zoom to 8
  if (zoomState.zoom >= 8) {
    if (zoomState.geoId && zoomState.geoId.length === 5) {
      return 'county';
    }
    return 'state';
  }
  return 'country';
}

export function zoomLevelIsCountry(zoomState) {
  return getZoomLevel(zoomState) === 'country';
}

export function zoomLevelIsState(zoomState) {
  return getZoomLevel(zoomState) === 'state';
}

export function zoomLevelIsCounty(zoomState) {
  return getZoomLevel(zoomState) === 'county';
}
